export class DireccionEntrega {
    calle
    altura
    piso
    departamento
    codigoPostal
    ciudad

    constructor(calle, altura, piso, departamento, codigoPostal, ciudad) {
        this.calle = calle
        this.altura = altura
        this.piso = piso
        this.departamento = departamento
        this.codigoPostal = codigoPostal
        this.ciudad = ciudad
    }

}